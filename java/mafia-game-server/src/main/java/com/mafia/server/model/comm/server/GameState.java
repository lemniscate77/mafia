package com.mafia.server.model.comm.server;

/**
 *
 * @author Just1689
 */
public class GameState extends ServerMessage {

    private String state;
    private String key;

    public GameState(String state, String key) {
        this.state = state;
        this.key = key;
    }

    /**
     * @return the state
     */
    public String getState() {
        return state;
    }

    /**
     * @param state the state to set
     */
    public void setState(String state) {
        this.state = state;
    }

    /**
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * @param key the key to set
     */
    public void setKey(String key) {
        this.key = key;
    }

}
